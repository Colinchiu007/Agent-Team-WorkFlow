# ===== 工具函数 =====

def run_cmd(cmd: List[str], workdir: Optional[Path] = None, check=True) -> subprocess.CompletedProcess:
    """运行命令并返回结果"""
    print(f"  $ {' '.join(cmd)}")
    if workdir:
        print(f"  (在 {workdir})")
    result = subprocess.run(cmd, cwd=workdir, capture_output=True, text=True, check=False)
    if result.returncode != 0 and check:
        print(f"  ❌ 命令失败退出码: {result.returncode}")
        print(f"  stderr: {result.stderr}")
        raise subprocess.CalledProcessError(result.returncode, cmd, result.stdout, result.stderr)
    return result


def gh_command(args: List[str]) -> subprocess.CompletedProcess:
    """封装 gh CLI"""
    return run_cmd(["gh"] + args)


def get_current_branch() -> str:
    """获取当前分支"""
    result = run_cmd(["git", "branch", "--show-current"], check=False)
    return result.stdout.strip() or "main"


def has_uncommitted_changes() -> bool:
    """检查是否有未提交变更"""
    result = run_cmd(["git", "status", "--porcelain"], check=False)
    return bool(result.stdout.strip())


def check_docs_sync(project_dir: Path, base_branch: str = "main") -> Tuple[bool, List[str]]:
    """
    检查文档同步（与 5 份核心文档对齐）

    返回：
        (docs_changed, files_to_update)
    """
    result = run_cmd(["git", "diff", f"origin/{base_branch}", "--name-only", "--pretty=format:"],
                     workdir=project_dir, check=False)

    changed_files = [f.strip() for f in result.stdout.strip().split("\n") if f.strip()]

    docs_to_update = []
    for file in CORE_DOCUMENTS:
        # 检查文件名（去除 docs/ 前缀）
        clean_file = file.replace("docs/", "")
        # 匹配：精确文件名 或 路径以这个文件结尾
        for f in changed_files:
            if f.endswith(clean_file) or f.split("/")[-1] == clean_file:
                docs_to_update.append(clean_file)
                break

    return len(docs_to_update) > 0, docs_to_update


