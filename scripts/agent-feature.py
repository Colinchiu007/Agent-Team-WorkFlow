def auto_commit(project_dir: Path, feature_key: str, change_type: str = "feat",
                scope: str = None, description: str = "添加新功能") -> str:
    """
    自动 Commit（严格遵循 Conventional Commits）

    Conventional Commits 格式：
        <type>(<scope>): <description>

        [optional body]

        [optional footer(s)]

    Args:
        project_dir: 项目目录
        feature_key: 功能 key（例：'fix-console-log-residue'）
        change_type: 变更类型（feat/fix/docs/style/refactor/test/chore/build/ci/perf）
        scope: 影响范围（默认从 feature_key 提取）
        description: 简短描述（必填）

    Returns:
        commit message
    """
    # 1. 验证 change_type
    valid_types = ["feat", "fix", "docs", "style", "refactor", "test", "chore", "build", "ci", "perf"]
    if change_type not in valid_types:
        print(f"  ⚠️  无效的 change_type '{change_type}'，回退到 'feat'")
        change_type = "feat"

    # 2. 提取 scope（如果未提供）
    if not scope:
        # 从 feature_key 提取首词
        words = feature_key.replace("-", "_").split("_")
        # 去掉 "fix" / "feat" 前缀
        words = [w for w in words if w not in ("fix", "feat")]
        scope = words[0] if words else "core"

    # 3. 限制 description 长度（≤72 字符）
    if len(description) > 72:
        description = description[:69] + "..."

    # 4. 构造完整 commit message
    message = f"{change_type}({scope}): {description}\n\n"

    # 5. body（根据 change_type 智能生成）
    if change_type == "feat":
        message += "- 添加实现代码\n"
        message += "- 添加单元测试\n"
    elif change_type == "fix":
        message += "- 修复 bug\n"
        message += "- 添加回归测试\n"
    elif change_type == "docs":
        message += "- 更新文档\n"
    elif change_type == "refactor":
        message += "- 重构代码（不改行为）\n"
    elif change_type == "test":
        message += "- 添加/修改测试\n"
    elif change_type == "chore":
        message += "- 配置/工具更新\n"
    elif change_type == "perf":
        message += "- 性能优化\n"
    else:
        message += "- 更新代码\n"

    # 6. footer：关联 Issue（如果有）
    if "fix" in feature_key or "bug" in description.lower():
        # Bug 修复尝试关联 Issue（自动获取最近创建的 fix Issue）
        message += "\nRefs: #auto-detect-from-current-branch\n"

    # 7. 限制总长度（避免 commit message 过长）
    if len(message) > 500:
        message = message[:497] + "..."

    print(f"\n💾 自动提交（Conventional Commits）:")
    print(f"  {message}")
    print()

    run_cmd(["git", "add", "."], workdir=project_dir)
    run_cmd(["git", "commit", "-m", message], workdir=project_dir)

    print("  ✅ Commit 已提交")
    return message


def auto_create_pr(project_dir: Path, feature_key: str, docs_updated: bool) -> Tuple[bool, str]:
    """
    自动创建 PR

    返回：(成功状态, PR URL) 或 (False, 错误信息)
    """
    print(f"\n🔗 自动创建 PR ({feature_key})")

    change_type = "docs" if docs_updated else "feat"
    title = f"[{change_type}] {feature_key}: 实现导出 CSV 功能"

    # 生成 PR body（含自动勾选的 checkbox）
    # 注意：gh CLI 的 --body 支持的多行需要 --body-file
    pr_description = generate_pr_body(feature_key, "", docs_updated)
    pr_description_file = project_dir / ".temp_pr_body.md"
    pr_description_file.write_text(pr_description, encoding='utf-8')

    try:
        result = gh_command([
            "pr", "create",
            "--title", title,
            "--body-file", str(pr_description_file),
            "--base", DEFAULT_BRANCH,
            "--head", f"@me:{feature_key}/v1"
        ])

        result.check_output()

        pr_url = result.stdout.strip()
        print(f"  ✅ PR 已创建: {pr_url}")

        return True, pr_url

    except subprocess.CalledProcessError as e:
        if "A pull request already exists" in e.stderr:
            print("  ⚠️  PR 已存在，跳过")
            return False, ""
        raise


